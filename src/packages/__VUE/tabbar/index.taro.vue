<template>
  <view :class="{ 'nut-tabbar__placeholder': bottom && placeholder }" :style="{ height: height + 'px' }">
    <view
      ref="nutTabbar"
      class="nut-tabbar"
      :class="{ 'nut-tabbar-bottom': bottom, 'nut-tabbar-safebottom': safeAreaInsetBottom }"
    >
      <slot></slot>
    </view>
  </view>
</template>

<script lang="ts">
import { onMounted, provide, reactive, ref, toRefs, watch } from 'vue';
import { createComponent } from '@/packages/utils/create';
import Taro from '@tarojs/taro';
import { TABBAR_KEY } from './types';
const { create } = createComponent('tabbar');
export default create({
  props: {
    modelValue: {
      type: [Number, String],
      default: 0
    },
    bottom: {
      type: Boolean,
      default: false
    },
    type: {
      type: String,
      default: 'base'
    },
    size: {
      type: String,
      default: '20px'
    },
    unactiveColor: {
      type: String,
      default: ''
    },
    activeColor: {
      type: String,
      default: ''
    },
    safeAreaInsetBottom: {
      type: Boolean,
      default: false
    },
    placeholder: {
      type: Boolean,
      default: false
    }
  },
  emits: ['tabSwitch', 'update:modelValue'],
  setup(props, { emit }) {
    const { bottom, placeholder } = toRefs(props);
    const mdValue = reactive({
      val: props.modelValue,
      children: []
    });
    const height = ref();
    function changeIndex(index: number, active: number | string) {
      emit('update:modelValue', active);
      parentData.modelValue = active;
      emit('tabSwitch', parentData.children[index], active);
    }
    const parentData = reactive({
      children: mdValue.children,
      size: props.size,
      modelValue: mdValue.val,
      unactiveColor: props.unactiveColor,
      activeColor: props.activeColor,
      changeIndex
    });
    provide(TABBAR_KEY, parentData);
    watch(
      () => props.modelValue,
      (value) => {
        parentData.modelValue = value;
      }
    );
    onMounted(() => {
      if (bottom.value && placeholder.value) {
        setTimeout(() => {
          const query = Taro.createSelectorQuery();
          query.select('.nut-tabbar').boundingClientRect();
          query.exec((res) => {
            height.value = res[0].height;
          });
        }, 500);
      }
    });
    return {
      changeIndex,
      height
    };
  }
});
</script>
